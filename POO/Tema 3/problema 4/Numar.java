public class Numar {
    private int num;

    public Numar(int num) {this.num = num;}

    public int suma(int a) {return num + a;}
    public int suma(int a, int b) {return num + a + b;}
    public int suma(int a, int b, int c) {return num + a + b + c;}
    public int suma(int a, int b, int c, int d) {return num + a + b + c + d;}

}
